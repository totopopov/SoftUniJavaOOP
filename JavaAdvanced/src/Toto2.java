
public class Toto2 {

    public static void main(String[] args) {

        for (int i = 10; i >=3 ; i--) {
            for (int j = i-1; j >=2 ; j--) {
                for (int k = j-1; k >=1 ; k--) {

                        System.out.println(i + " " + j + " "+k );

                }
            }
        }
    }
}
